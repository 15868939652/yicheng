# 各平台患者/自媒体概率权重
# 头条、知乎：固定患者第一视角
# 搜狐、百家号：固定自媒体创作者视角
_PLATFORM_PROFILE_WEIGHTS = {
    "toutiao":   (1.0, 0.0),  # 固定患者视角
    "zhihu":     (1.0, 0.0),  # 固定患者视角
    "sohu":      (0.0, 1.0),  # 固定自媒体
    "baijiahao": (0.0, 1.0),  # 固定自媒体
}


def random_profile(platform: str = "") -> str:
    """根据平台设定患者/自媒体概率，返回随机人设"""
    patient_w, media_w = _PLATFORM_PROFILE_WEIGHTS.get(platform, (0.50, 0.50))
    group = random.choices(["patient", "media"], weights=[patient_w, media_w])[0]
    if group == "patient":
        return random.choice(_PATIENT_PROFILES)
    else:
        return random.choice(_MEDIA_PROFILES)


def random_style(profile: str = "") -> str:
    """根据人设类型匹配风格库，默认随机"""
    is_media = any(kw in profile for kw in ["账号", "博主", "运营", "创作者", "公众号", "自媒体"])
    if is_media:
        return random.choice(_MEDIA_STYLES)
    return random.choice(_PATIENT_STYLES)


def random_trigger() -> str:
    return random.choice(_TRIGGERS)
